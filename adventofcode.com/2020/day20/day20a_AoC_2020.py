chunks = puzzleinput.split("\n\n")
data = set()
for chunk in chunks :
    number,tile = chunk.split("\n",1)
    number = int(number[5:-1])
    tile = tile.split()
    left_edge = "".join(row[0] for row in tile)
    right_edge = "".join(row[-1] for row in tile)
    edges = tile[0],"".join(reversed(tile[0])),tile[-1],"".join(reversed(tile[-1])),left_edge,"".join(reversed(left_edge)),right_edge,"".join(reversed(right_edge))
    data.add((number,edges))
product = 1
for number,edges in data :
    all_edges = set()
    for other_number,other_edges in data - {(number,edges)} :
        for edge in other_edges :
            all_edges.add(edge)
    if len(set(edges) & all_edges) == 4 :
        product *= number
